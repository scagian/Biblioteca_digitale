    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jEditorPane1.getText() != null) {
            setCursor(new java.awt.Cursor(java.awt.Cursor.WAIT_CURSOR));
            Object[] param = {jEditorPane1.getText(), this.pagina, this.utente, this.opera};
            HashMap<String, Object[]> tras = new HashMap<String, Object[]>();
            tras.put("trascrivi", param);
            MyThread progress = new MyThread(jProgressBar1);
            progress.start();
            if (this.controllerP.doOperation(tras).equals("true")) {
                setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
                JOptionPane.showMessageDialog(jFrame1, "Trascrizione avvenuta correttamente!");
            } else {
                setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
                JOptionPane.showMessageDialog(jFrame1, "Errore durante la trascrizione"); 
            }
            this.dispose();
            Pannello_trascrittore new_p = new Pannello_trascrittore
            (this.controllerP.askOpere(this.utente, "acquisito"),
            this.controllerP);
            if (prev != null) {
               Trascrittore_pagine new_tp = new Trascrittore_pagine(this.controllerP.askPagine(this.opera, this.utente), 
               this.controllerP, new_p);
               if (new_tp.sizeList()>0) {
                    new_tp.setVisible(true);  
               } else {
                    new_p.setVisible(true);
               }
               prev.dispose();
            } else {
               new_p.setTable(1);
               new_p.setVisible(true);
               _prev.dispose();
            }        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

